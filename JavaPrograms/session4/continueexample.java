package session4;

public class continueexample {

	public static void main(String[] args) {
		
		for(int i =1; i<=10; i++)  // for loop use when we know the limit for run and while/do while using when we are not know about how many time run
		{
			if(i==5)
				continue;
			System.out.println(i); //Initialization /condition check / increment or decrement
		}

	}

}
