package Session3;

public class elseifLadder {

	public static void main(String[] args) {
		int day = 1;
		
		if(day==1) //1st condition
		{
			System.out.println("Sunday");
		}
		else if(day==2) //2nd condition
		{
			System.out.println("Monday");
		}
		else if(day==3) //3rd condition
		{
			System.out.println("Tueday");
		}
		else if(day==4) //4th condition
		{	
			System.out.println("Wednesday");
		}
		else if(day==5) //5th condition
		{
			System.out.println("Thursday");
		}
		else if(day==5) //6th condition
		{
			System.out.println("Friday");
		}
		else if(day==6) //7th condition
		{
			System.out.println("Saturday");
		}
		else
		{
			System.out.println("Wrong input for the day value");
		}
}
}	